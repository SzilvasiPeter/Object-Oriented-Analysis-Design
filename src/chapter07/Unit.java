package chapter07;

import java.util.Map;

public class Unit {

    private String type;
    private Map properties;

    public Unit() {

    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Map getProperties() {
        return properties;
    }

    public void setProperties(Map properties) {
        this.properties = properties;
    }
}
